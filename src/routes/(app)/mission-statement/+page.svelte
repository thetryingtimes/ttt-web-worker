<Article>
  <h1>Mission Statement</h1>
  <p>
    The Trying Times is an independent publication seeking to improve media
    literacy, increase government transparency, and fund people-powered
    initiatives.
  </p>
  <h2>Improving media literacy</h2>
  <p>
    Too often, major media outlets present one side of the story — a disservice
    to American democracy that has resulted in a convicted felon and
    twice-impeached former president’s return to office.
  </p>
  <p>
    Our objective is simple: by focusing on legislation (executive orders, draft
    bills, and court decisions — not tweets), we offer a unique framing of how
    the government actually works by addressing the forecasted impact of a
    particular bill on the lives of regular Americans.
  </p>
  <h2>Increasing government transparency</h2>
  <p>
    By going straight to the legislation, we aim to side-step the narrative spun
    by politicos gunning for power and news outlets seeking clicks by peddling
    outrage.
  </p>
  <p>
    Focusing on outcomes of legislation — without a partisan bias — allows our
    readers to better understand how their government works for (and against)
    their interests.
  </p>
  <p>
    Similarly, TTT acts as a bellwether for government officials seeking
    real-time support of their sponsored legislation. A win-win scenario, with a
    powerful remedy through our use of platform profits.
  </p>
  <h2>Funding people-powered initiatives</h2>
  <p>
    Each story we cover, and its forecasted impact, can be voted on by our
    readers. We take special care to ensure our voting ledger is accurate (that
    is, only readers in the US with a valid account can vote) so as to build a
    true sense of the people’s opinion.
  </p>
  <p>
    Several times throughout the year, we disburse our publication’s profits to
    existing nonprofits, NGOs, PACs, and any organization or charitable cause
    that aligns with the will of our readers. Our north star is to support
    democracy by harnessing the power of individuals’ attention for good,
    impacting the trajectory of American democracy by contributing in a way that
    drives results: financially.
  </p>
</Article>
